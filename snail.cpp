#include <bits/stdc++.h>
#include <vector>
using namespace std;

vector <int> snail(const vector<vector<int>> &snail_map){
  vector <int> res;
//  cout << snail_map.size() << endl;
  for(int i=0; i < snail_map.size(); i++){
    for(int j=0; j < snail_map.size(); j++){
      res.push_back[j];
    }
  }
  return res;
}

int main(){
  cout << snail({{1,2,3,4}, {5,6,7,8}}) << endl;
  return 0;


}
